package main

import (
	"fmt"
	"html/template"
	"net/http"
	"strings"
)

var (
	temp *template.Template
	out  string
)

func main() {
	temp = template.Must(template.ParseGlob("*.html"))
	fs := http.FileServer(http.Dir("style"))
	http.Handle("/style/", http.StripPrefix("/style", fs))
	http.HandleFunc("/", html)
	http.ListenAndServe(":8080", nil)
}

func html(w http.ResponseWriter, r *http.Request) {
	Oinput := r.FormValue("Input1")
	Font := r.FormValue("font")
	ascii := ascii(Oinput, Font)
	temp.ExecuteTemplate(w, "web.html", ascii)
}

//////////////////////////
//    checks argument   //
//////////////////////////

func ascii(input string, font string) string {
	//////////////////////////
	//  specify & use input //
	//////////////////////////
	if input == "" {
		fmt.Print("")
	} else if input == "\r\n" {
		fmt.Println()
	} else if font == "standard" {
		return standard(input)
	} else if font == "shadow" {
		return shadow(input)
	} else if font == "thinkertoy" {
		return thinkertoy(input)
	} else {
		fmt.Print("Usage: go run main.go [string] [font]\nEx: go run main.go \"Hello\" shadow\nFonts:\n- Standard\n- Shadow\n- Thinkertoy")
	}
	return input
}

